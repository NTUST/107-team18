  for id_ in ids:
    url = 'http://info.ntust.edu.tw/faith/edua/app/qry_linkoutline.aspx?semester=1071&courseno={}'.format(id_)
    res = requests.get(url)
    soup = BeautifulSoup(res.text, 'html.parser')
    name = soup.find("span", {"id": "lbl_coursename"}).find("font").text # 課程名稱
    teacher = soup.find("span", {"id": "lbl_teacher"}).find("font").text # 課程老師
